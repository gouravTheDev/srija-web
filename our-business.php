<h2>Why develop a mobile app?</h2>
      <p>Use of mobile  devices is set to overtake desktop for accessing websites. Think about how  often you use your phone, or you see other people using theirs. It's common to  use your mobile to browse websites, search for information and make purchases.  A mobile app enhances the user experience further and achieves a greater  engagement with your users. Often a mobile app offers features that a mobile  web site (or web app) isn't capable of because it can have greater access to  the mobile device's hardware (such as GPS, camera and microphone).</p>
