We'll work to ensuring that product continues to perform in the way that  it&rsquo;s expected long after the product is launched. Long term support for mobile  apps is a key to its success as new devices and device updates often change the  underlying systems which could otherwise harm performance of the application.  And as user uptake of your application grows, we'll work with you to collect  and analyze information about usage and help to guide design changes and  enhancements. We like to maintain relationships with clients over the years,  supporting systems, offering advice and remaining a part of the project.
      <h2>Native, web and hybrid apps</h2>
      <p>There are  options for the type of mobile app you can build, depending on how you want it  to be used. A native app is specific to, for example, Android or iOS (Apple).  Generally, the result is slicker and perhaps better access to things such as  accelerator, camera, location, because it has built specifically for that  device. Mobile web apps are applications run via a website and can be seen in  the browser on your mobile phone. A hybrid app is a HTML5, CSS and Java script  web application (the same technology used for normal websites), that is then  adapted to be used on various devices. Hybrid apps are installed in the same  way (through App Store or Play Store), so depending on the features and/or  design, the user may never be aware of the difference. Hybrid apps do have some  limitations, but they have the benefit of being much quicker to develop and  deploy to different devices types.</p>
