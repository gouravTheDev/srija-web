<h2>What are web apps and how can they help me?</h2>
      <p>Webs apps are  pieces of online software that fulfill a purpose. A web app can be part of a  website or a separate software tool. It does not need to be installed, as users  access it through their web browser. For example, you may already have a  website but need an application to provide an online portal for subscribers. Or  you may need a complex, clever application that will take huge amounts of raw  data and extract certain information from it. An app - whatever it may be -  performs a function.</p>
