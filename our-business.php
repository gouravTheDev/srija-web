<h2>Why choose web over desktop?</h2>
      <p>A web app is often  chosen due to ease of maintenance and support. Rather than having software  installed on your computer, it is hosted centrally in one place, and you login  to access. If there is a problem, an engineer does not usually need to look at  your computer. A device dependent desktop application will have limitations on  the type of computer it can be used on, for example developing something with  the Microsoft language has to run on Windows computers. A web app has greater  flexibility. Web apps do have limitations depending on the user having access  to a web browser, and an internet connection - although this is much less of a  limitation than in the past.</p>
